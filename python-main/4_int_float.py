# Tipos inte float
# int -> Número inteiro
# O tipo int representa qualquer numero 
# positivo ou negativo. int sem sinal é considerado
# positivo
print(11) #int
print(-11) #int
print(0) #int

# float -> Número com ponto flutuante
# O tipo float representa qualquer numero
# posito ou negativo com ponto flutuante
# float sem sinal é considerado positivo
print(1.1, 300) #float
print(0.0, -0.0) # 0 tbm pode ser float e negativo tbm pode ser float

# A função type mostra o tipo que o python 
# inferiu o valor.
print(type('Otavio'))
print(type(-1))
print(type(2.5))